export function getStyleById(id: string): Style | undefined {
  return styles.find((style) => style.id === id);
}

export function getStylesByCategory(category: string): Style[] {
  if (category === "All") return styles;
  return styles.filter((style) => style.category === category);
}

export function searchStyles(query: string): Style[] {
  const lowerQuery = query.toLowerCase();
  return styles.filter(
    (style) =>
      style.name.toLowerCase().includes(lowerQuery) ||
      style.category.toLowerCase().includes(lowerQuery) ||
      style.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)) ||
      style.promptTemplate.toLowerCase().includes(lowerQuery)
  );
}

export function getCategories(): string[] {
  const categories = new Set(styles.map((style) => style.category));
  return ["All", ...Array.from(categories).sort()];
}
